package com.klpdapp.klpd.dto;

public class attributedto {
    private String attId;         
    private String diameter;
    private String thickness;
    private String capacity;
    private String cartonDimension;
    private String weight;
    private String guarantee;
    private String color;
    public String getAttId() {
        return attId;
    }
    public void setAttId(String attId) {
        this.attId = attId;
    }
    public String getDiameter() {
        return diameter;
    }
    public void setDiameter(String diameter) {
        this.diameter = diameter;
    }
    public String getThickness() {
        return thickness;
    }
    public void setThickness(String thickness) {
        this.thickness = thickness;
    }
    public String getCapacity() {
        return capacity;
    }
    public void setCapacity(String capacity) {
        this.capacity = capacity;
    }
    public String getCartonDimension() {
        return cartonDimension;
    }
    public void setCartonDimension(String cartonDimension) {
        this.cartonDimension = cartonDimension;
    }
    public String getWeight() {
        return weight;
    }
    public void setWeight(String weight) {
        this.weight = weight;
    }
    public String getGuarantee() {
        return guarantee;
    }
    public void setGuarantee(String guarantee) {
        this.guarantee = guarantee;
    }
    public String getColor() {
        return color;
    }
    public void setColor(String color) {
        this.color = color;
    }
}
